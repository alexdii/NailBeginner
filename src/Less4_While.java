public class Less4_While {
    public static void main(String[] args) {
        int value = 0;
        while (value<5) {
            System.out.println("Hello " + value);
            value++;
        }
        // в цикле while нужен какой-нибудь логический оператор - до каких пор будет выполняться условие
        // while - это цикл, который выполняется до тех пор, пока условие в круглых скобках дает true
    }
}
